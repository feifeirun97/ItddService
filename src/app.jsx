export async function getInitialState() {
  const fetchUserInfo = async () => {
    try {
      if (cookies.load(config.appId + "_token") === undefined) {
        throw 'login'
      }
      

      const msg = {
        success:true,
        data:{
          "name": cookies.load(config.appId + "_account"),
          "avatar": "https://gw.alipayobjects.com/zos/antfincdn/XAosXuNZyF/BiazfanxmamNRoxxVxka.png",
          "userid":cookies.load(config.appId + "_userId"),
          "country": "China",
          "access": "user",
        }
      }

      return msg.data;

      
    } catch (error) {
      console.log(error)
      history.push(loginPath);
    }

    return undefined;
  }; // 如果是登录页面，不执行

  if (history.location.pathname !== loginPath) {
    const currentUser = await fetchUserInfo();
    return {
      fetchUserInfo,
      currentUser,
      settings: {},
    };
  }

  return {
    fetchUserInfo,
    settings: {},
  };
} // import * as  echarts from "echarts"; 支持的api https://procomponents.ant.design/components/layout

